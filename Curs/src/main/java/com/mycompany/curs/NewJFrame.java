public static class HuffmanZipDecompressor {
        public static void decompressZipToDocx(String zipPath, String docxPath) 
            throws IOException, ClassNotFoundException {

            try (ZipInputStream zis = new ZipInputStream(new FileInputStream(zipPath))) {
                ZipEntry entry = zis.getNextEntry();
                
                if (!"compressed.huff".equals(entry.getName())) {
                    throw new IOException("Неверный формат архива");
                }
                
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                byte[] buffer = new byte[1024];
                int len;
                while ((len = zis.read(buffer)) > 0) {
                    baos.write(buffer, 0, len);
                }

                
                try (ObjectInputStream ois = new ObjectInputStream(new ByteArrayInputStream(baos.toByteArray()))) {
                    @SuppressWarnings("unchecked")
                    Map<Byte, String> huffmanCodes = (Map<Byte, String>) ois.readObject();

                    int originalSize = ois.readInt();

                    byte[] compressedData = ois.readAllBytes();


                    byte[] originalData = decompressWithHuffman(compressedData, huffmanCodes, originalSize);
                    
                    Files.write(Paths.get(docxPath), originalData);

                }
            }
        }
        
        
        public static byte[] decompressFromNetwork(byte[] networkData) throws IOException, ClassNotFoundException {
        try (ObjectInputStream objStream = new ObjectInputStream(new ByteArrayInputStream(networkData))) {
            // 1. Проверка сигнатуры
            String signature = objStream.readUTF();
            if (!"HUFFMAN_v1".equals(signature)) {
                throw new IOException("Неверный формат сетевых данных");
            }
            
            // 2. Чтение метаданных
            @SuppressWarnings("unchecked")
            Map<Byte, String> huffmanCodes = (Map<Byte, String>) objStream.readObject();
            int originalSize = objStream.readInt();
            byte[] compressedBytes = objStream.readAllBytes();
            
            // 3. Распаковка
            return decompressWithHuffman(compressedBytes, huffmanCodes, originalSize);
        }
    }
        
        
        public static byte[] decompressWithHuffman(byte[] compressedData, 
                                         Map<Byte, String> huffmanCodes,
                                         int originalSize) {
            Map<String, Byte> reverseCodes = new HashMap<>();
            for (Map.Entry<Byte, String> entry : huffmanCodes.entrySet()) {
                reverseCodes.put(entry.getValue(), entry.getKey());
            }

            StringBuilder bitString = new StringBuilder();
            for (byte b : compressedData) {
                String binaryString = String.format("%8s", Integer.toBinaryString(b & 0xFF)).replace(' ', '0');
                bitString.append(binaryString);
            }

            ByteArrayOutputStream baos = new ByteArrayOutputStream();
            StringBuilder currentCode = new StringBuilder();
            
            for (int i = 0; i < bitString.length(); i++) {
                currentCode.append(bitString.charAt(i));
                if (reverseCodes.containsKey(currentCode.toString())) {
                    baos.write(reverseCodes.get(currentCode.toString()));
                    currentCode.setLength(0);
                    
                    if (baos.size() == originalSize) {
                        break;
                    }
                }
            }
            
            return baos.toByteArray();
        }
    }


    
    
    
    
