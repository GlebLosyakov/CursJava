private byte[] sendToServer(String command, byte[] fileData, String fileName) throws IOException, ClassNotFoundException {
    try (Socket socket = new Socket(jTextField1.getText().trim(), 12345);
         ObjectOutputStream oos = new ObjectOutputStream(socket.getOutputStream());
         ObjectInputStream ois = new ObjectInputStream(socket.getInputStream())) {
        
        // Отправляем команду и данные
        oos.writeUTF(command);
        oos.writeObject(fileData);
        oos.writeUTF(fileName);
        oos.flush();
        
        // Для декомпрессии нужно отправить дополнительные данные
        if ("decompress".equals(command)) {
            try (ObjectInputStream fileOis = new ObjectInputStream(new ByteArrayInputStream(fileData))) {
                @SuppressWarnings("unchecked")
                Map<Byte, String> huffmanCodes = (Map<Byte, String>) fileOis.readObject();
                int originalSize = fileOis.readInt();
                
                oos.writeObject(huffmanCodes);
                oos.writeInt(originalSize);
                oos.flush();
            }
        }
        
        // Получаем результат
        byte[] result = (byte[]) ois.readObject();
        String resultFileName = ois.readUTF();
        
        return result;
    }
}
    
